<h1>Brownies</h1>

<p>
    1 stick (1/2 cup, 113g) butter<br>
    1/2 cup (50g) cocoa powder (l like dutch)<br>
    1 14 oz (396g) can sweetened condensed milk<br>
    1 cup (120g) powdered sugar<br>
    2 teaspoons vanilla<br>
    2 teaspoons molasses (optional; reduce if not getting good skin)<br>
    About 1/4 tsp salt (if used unsalted butter)<br>
    1 egg yolk<br>
    3/4 cup (90g) all-purpose flour<br>
    1 cup chocolate chips (optional, I prefer this recipe without them)<br>
    325ºF, 160ºC Oven<br>
    9-inch (23 cm) square pan<br>
    <br>
    Brown butter<br>
    Whisk in cocoa powder, then take off heat<br>
    Whisk in the condensed milk, then sugar until dissolved<br>
    Whisk in the salt, vanilla, molasses, and egg yolk<br>
    Whisk in the flour<br>
    Cool before adding chocolate chips<br>
    Bake 30-40 min, until a toothpick to the center comes out clean,<br>
    or not-quite-clean if you prefer fudgy brownies<br>
    Let cool thoroughly before cutting<br>
    <br>
